#include "libft.h"

void	ft_putnbr_fd(int n, int fd)

{
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n *= -1;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		n = n % 10;
	}
	if (n >= 0)
	{
		n = n + '0';
		ft_putchar_fd(n, fd);
	}
}
